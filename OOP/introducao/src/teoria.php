Classe:
 - uma forma/molde para criação de objetos.
 - ao criar uma classe está criando um novo tipo especifico, diferente dos primitivos (ex: int, string).
 
Objetos : 
 - O mesmo que uma instância de uma classe

 Abstração: 
  - Conceito do mundo real, onde, dentro do sistema só serão utilizados (abstraido para dentro do sistema) somente o que é nescessário para ele. Mesmo que no mundo real exista diversos outros atributos. 
 